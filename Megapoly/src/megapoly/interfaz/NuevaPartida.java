    private void bComenzarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bComenzarPartidaActionPerformed
        // TODO add your handling code here:
        Dimension dim;
        dim = super.getToolkit().getScreenSize();
        if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("2 Jugadores")) {
            VentanaPartida partida = new VentanaPartida(jugador1, jugador2);
            partida.setSize(dim);
            partida.setVisible(true);
        } else if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("3 Jugadores")) {
            VentanaPartida partida = new VentanaPartida(jugador1, jugador2, jugador3);
            partida.setSize(dim);
            partida.setVisible(true);
        } else if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("4 Jugadores")) {
            VentanaPartida partida = new VentanaPartida(jugador1, jugador2, jugador3, jugador4);
            partida.setSize(dim);
            partida.setVisible(true);
        }


    }//GEN-LAST:event_bComenzarPartidaActionPerformed
