    private void cbNumeroJugadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbNumeroJugadoresActionPerformed
        // TODO add your handling code here:

        if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("2 Jugadores")) {
            

            nombrej1.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 1", 1));
            nombrej2.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 2", 1));
            
            nombrej1.setVisible(true);
            nombrej2.setVisible(true);

            jugador1 = new Jugador(nombrej1.getText());
            jugador2 = new Jugador(nombrej2.getText());

        } else if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("3 Jugadores")) {
            nombrej1.setVisible(true);
            nombrej2.setVisible(true);
            nombrej3.setVisible(true);

            nombrej1.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 1", 1));
            nombrej2.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 2", 1));
            nombrej3.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 3", 1));

            jugador1 = new Jugador(nombrej1.getText());
            jugador2 = new Jugador(nombrej2.getText());
            jugador3 = new Jugador(nombrej3.getText());
        } else if (cbNumeroJugadores.getSelectedItem().toString().equalsIgnoreCase("4 Jugadores")) {
            nombrej1.setVisible(true);
            nombrej2.setVisible(true);
            nombrej3.setVisible(true);
            nombrej4.setVisible(true);

            nombrej1.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 1", 1));
            nombrej2.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 2", 1));
            nombrej3.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 3", 1));
            nombrej4.setText(JOptionPane.showInputDialog(null, "Nombre", "Nombre del jugador 4", 1));

            jugador1 = new Jugador(nombrej1.getText());
            jugador2 = new Jugador(nombrej2.getText());
            jugador3 = new Jugador(nombrej3.getText());
            jugador4 = new Jugador(nombrej4.getText());
        }
    }//GEN-LAST:event_cbNumeroJugadoresActionPerformed

